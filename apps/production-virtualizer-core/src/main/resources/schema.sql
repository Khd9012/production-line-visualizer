CREATE TABLE IF NOT EXISTS EQ_ROUTE_ORDER (
    CMD_ID VARCHAR(50) PRIMARY KEY,
    EQ_CODE VARCHAR(50),
    EQ_TYPE VARCHAR(50),
    CMD_STATUS VARCHAR(20),
    CMD_STATUS_DESC VARCHAR(255),
    WORK_IDX VARCHAR(50),
    WORK_ID VARCHAR(50),
    BARCODE VARCHAR(100),
    F_EQ_CODE VARCHAR(50),
    F_DEVICE_CODE VARCHAR(50),
    T_EQ_CODE VARCHAR(50),
    T_DEVICE_CODE VARCHAR(50),
    F_LINK_EQ_CODE VARCHAR(50),
    F_LINK_DEVICE_CODE VARCHAR(50),
    PARENT_EQ_CODE VARCHAR(50),
    PARENT_DEVICE_CODE VARCHAR(50),
    C_EQ_CODE VARCHAR(50),
