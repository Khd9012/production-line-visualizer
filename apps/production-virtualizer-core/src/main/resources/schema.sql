CREATE SEQUENCE IF NOT EXISTS SEQ_CMD_ID START WITH 1 INCREMENT BY 1;

CREATE TABLE IF NOT EXISTS MD_EQ (
    EQ_CODE VARCHAR(50) PRIMARY KEY
);

CREATE TABLE IF NOT EXISTS MD_DEVICE (
    EQ_CODE VARCHAR(50) NOT NULL,
    DEVICE_CODE VARCHAR(50) NOT NULL,
    DEVICE_TYPE VARCHAR(50),
    DEVICE_NAME VARCHAR(100),
    DEVICE_AREA_CODE VARCHAR(50),
    CFG_CODE VARCHAR(50),
    PRIMARY KEY (EQ_CODE, DEVICE_CODE)
);

CREATE TABLE IF NOT EXISTS MD_DEVICE_STATUS_ADDR (
    CFG_CODE VARCHAR(50) NOT NULL,
    EQ_CODE VARCHAR(50) NOT NULL,
    DEVICE_CODE VARCHAR(50) NOT NULL,
    DEVICE_STATUS_ADDR_CODE VARCHAR(50),
    PRIMARY KEY (CFG_CODE, EQ_CODE, DEVICE_CODE, DEVICE_STATUS_ADDR_CODE)
);

CREATE TABLE IF NOT EXISTS MD_DEVICE_ERROR_CODE_INFO (
    CFG_CODE_TYPE VARCHAR(20) NOT NULL,
    ERROR_CODE VARCHAR(20) NOT NULL,
    DEVICE_TYPE VARCHAR(50) NOT NULL,
    ERROR_DESC VARCHAR(255),
    ERROR_LEVEL INT DEFAULT 0,
    NOTE VARCHAR(255),
    PRIMARY KEY (CFG_CODE_TYPE, ERROR_CODE, DEVICE_TYPE)
);

CREATE TABLE IF NOT EXISTS MD_DEVICE_ERROR_NOTI (
    SEQ BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EQ_CODE VARCHAR(50),
    DEVICE_CODE VARCHAR(50),
    DEVICE_TYPE VARCHAR(50),
    DEVICE_NAME VARCHAR(100),
    ERROR_CODE VARCHAR(20),
    ERROR_DESC VARCHAR(255),
    CLEAR_YN CHAR(1) DEFAULT 'N',
    CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATE_DATE TIMESTAMP
);

CREATE TABLE IF NOT EXISTS MD_DEVICE_CTRL (
    CFG_CODE VARCHAR(50) PRIMARY KEY,
    CFG_NAME VARCHAR(100),
    CFG_MODEL VARCHAR(20),
    CTRL_IP VARCHAR(50),
    CTRL_PORT INT,
    CTRL_RS_COM VARCHAR(50),
    INF_TYPE VARCHAR(50),
    INF_MODEL VARCHAR(50),
    DATA_TYPE VARCHAR(50),
    USE_YN CHAR(1),
    STATUS_CODE VARCHAR(20),
    STATUS_DESC VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS MD_DEVICE_PLACE (
    EQ_CODE VARCHAR(50) NOT NULL,
    DEVICE_CODE VARCHAR(50) NOT NULL,
    MOTION_TYPE VARCHAR(20),
    PRIMARY KEY (EQ_CODE, DEVICE_CODE, MOTION_TYPE)
);

CREATE TABLE IF NOT EXISTS MD_BCR_READING_HISTORY (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DEVICE_AREA_CODE VARCHAR(50),
    BARCODE VARCHAR(100),
    WORK_ID VARCHAR(50),
    CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS DB_STO_STOMST (
    WHID VARCHAR(20),
    LOCT VARCHAR(50) PRIMARY KEY,
    RACK_STS VARCHAR(20),
    STOCK_STS VARCHAR(20)
);

CREATE TABLE IF NOT EXISTS DB_ORDER_PALLET_PART_INFO (
    MASTER_KEY VARCHAR(50) NOT NULL,
    MASTER_SEQ VARCHAR(50) NOT NULL,
    ORDER_NO VARCHAR(50) NOT NULL,
    PTN_GROUP VARCHAR(50),
    PLT_NO INT NOT NULL,
    BOX_ORDER_QTY INT DEFAULT 0,
    BOX_ROBO_QTY INT DEFAULT 0,
    REMARKS VARCHAR(255),
    CASE_NO VARCHAR(50),
    ROBO_NO INT,
    ROBO_LINE VARCHAR(20),
    M_BOX_CD VARCHAR(50),
    PRIMARY KEY (MASTER_KEY, MASTER_SEQ, ORDER_NO, PLT_NO, CASE_NO)
);

CREATE TABLE IF NOT EXISTS DB_ORDER_PALLET_BOX_INFO (
    MASTER_KEY VARCHAR(50) NOT NULL,
    MASTER_SEQ VARCHAR(50) NOT NULL,
    ORDER_NO VARCHAR(50) NOT NULL,
    PLT_NO INT NOT NULL,
    LN_IPUT_NO VARCHAR(50),
    PTN_GROUP VARCHAR(50),
    PART_NO VARCHAR(50),
    TICKET_BCD VARCHAR(100) NOT NULL,
    SERIAL_NO VARCHAR(100),
    PRIMARY KEY (MASTER_KEY, MASTER_SEQ, ORDER_NO, PLT_NO, TICKET_BCD)
);

CREATE TABLE IF NOT EXISTS EQ_PLACE_ORDER (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    WORK_ID VARCHAR(50),
    CMD_STATUS VARCHAR(20),
    CMD_STATUS_DESC VARCHAR(255),
    BARCODE VARCHAR(100),
    M_BOX_CD VARCHAR(50),
    DEVICE_CODE VARCHAR(50),
    CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS EQ_APM_PLACE_ORDER (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    WORK_ID VARCHAR(50),
    PTN_GROUP VARCHAR(50),
    MASTER_KEY VARCHAR(50),
    MASTER_SEQ VARCHAR(50),
    ORDER_NO VARCHAR(50),
    PLT_NO INT,
    ORDER_TYPE VARCHAR(50),
    RECIPE_SERIAL VARCHAR(500),
    CMD_ID VARCHAR(50),
    CMD_STATUS VARCHAR(20)
);

CREATE TABLE IF NOT EXISTS DB_PALLET_LABEL_BUFFER (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    WORK_ID VARCHAR(50),
    PTN_GROUP VARCHAR(50),
    MASTER_KEY VARCHAR(50),
    MASTER_SEQ VARCHAR(50),
    ORDER_NO VARCHAR(50),
    PLT_NO INT,
    MOD_DTM TIMESTAMP
);

CREATE TABLE IF NOT EXISTS DB_ROBO_DATA_INFO (
    ROBO_NO INT NOT NULL,
    ROBO_LINE VARCHAR(20) NOT NULL,
    MASTER_KEY VARCHAR(50),
    MASTER_SEQ VARCHAR(50),
    ORDER_NO VARCHAR(50),
    PLT_NO INT,
    PRIMARY KEY (ROBO_NO, ROBO_LINE)
);

